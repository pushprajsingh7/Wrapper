import java.util.*;

/**
 * program which generates the minimum and maximum value for each of the Numeric
 * Wrapper classes (Byte, Short, nteger, Long, Float, Double)
 * 
 * @author ss
 *
 */

public class Highestvalue {
	public static void main(String args[]) {

		System.out.println(Double.MAX_VALUE + 1);
		System.out.println("mplus 1" + Double.MAX_VALUE + 1);
		System.out.println(Double.MIN_VALUE);
		System.out.println("xyz" + Integer.MAX_VALUE);
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Float.MIN_VALUE);
		System.out.println(Float.MAX_VALUE);
		System.out.println(Byte.MIN_VALUE);
		System.out.println(Byte.MAX_VALUE);
		System.out.println(Character.MAX_VALUE);
		System.out.println(Character.MIN_VALUE);
		System.out.println(Short.MIN_VALUE);
		System.out.println(Short.MAX_VALUE);
		System.out.println(Long.MIN_VALUE);
		System.out.println(Long.MAX_VALUE);
	}

}
